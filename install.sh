# Function to prompt for input with validation
prompt_input() {
    local prompt="$1"
    local default="$2"
    local var_name="$3"
    local required="$4"
    local input=""

    while true; do
        echo -n "$prompt"
        if [ -n "$default" ]; then
            echo -n " (default: $default)"
        fi
        if [ "$required" = "true" ]; then
            echo -n " [REQUIRED]"
        fi
        echo -n ": "
        read input

        # Use default if input is empty and default exists
        if [ -z "$input" ] && [ -n "$default" ]; then
            input="$default"
        fi

        # Check if required field is empty
        if [ "$required" = "true" ] && [ -z "$input" ]; then
            echo "âŒ This field is required. Please enter a value."
            continue
        fi

        # Validate URL format for server URL
        if [ "$var_name" = "SERVER_URL" ] && [ -n "$input" ]; then
            if [[ ! "$input" =~ ^https?:// ]]; then
                echo "âŒ Server URL must start with http:// or https://"
                echo "   Example: https://myserver.com or https://12.34.56.78"
                continue
            fi
        fi

        break
    done

    eval "$var_name='$input'"
}

